    //attburðarhandler fyrir bakruns litur
    private void jBakrunsLiturMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBakrunsLiturMousePressed
        // býr til LitirDialog
        LitirDialog f = new LitirDialog(this, true);
        //birtir LitirDialoginn
        f.setVisible(true);
        //nær í valin lit
        Color c = f.getValinLitur();
        
        if(f.erOk()){
            //geir bakkruns litinn að vitnum sem er vallinn
            jTeikniBlad.setBackground(c);
            ((MyndPanel)jTeikniBlad).lagaStrokledur();
            
        }
    }//GEN-LAST:event_jBakrunsLiturMousePressed

