    //fall sem sér um að setja réttan lit
    private void litaVinsla(Color c){
        //stilir teikni litin í mynda panelnu
        ((MyndPanel)jTeikniBlad).setLitur(c);
        //setur rétta litinn
        vinsla.setColor(c);
        //uppfærir stærð pannelið
        ((StærðPanel)jStærð).teiknaStærð(vinsla.getStærð(), vinsla.getColor(), vinsla.getAdgerd());
    }
    //fal sem setur stærð á slider
    private void setjaSlider(int i){
        //setur gildi á slider
        jSliderStærð.setValue(i);
        //setur rétt gildi vinslu
        vinsla.setStærð(i);
        //setur gildi í teikniblað
        ((MyndPanel)jTeikniBlad).setStærð(i);
        //uppfærir mynd í stærð pannel
        ((StærðPanel)jStærð).teiknaStærð(vinsla.getStærð(), vinsla.getColor(), vinsla.getAdgerd());
    }
    
    
