    //fall sem stopar forit
    private void haeta(){
        //spirr notenda hvort hann vilji vista mynd
        if (JOptionPane.showConfirmDialog(this, "Villtu vista mynd áður en þú hættir ", "Vista mynd", JOptionPane.YES_NO_OPTION)==0){
            //fall sem vistar vistar mynd
            Vista();
        }
        // slekur á forriti
        System.exit(0);
    }
    //fall sem vistar jPanelið sem PNG
    private void Vista(){
        //býr til FileChoser
        JFileChooser chooser = new JFileChooser();
        //breitir titli
        chooser.setDialogTitle("Vista Skrá");
        //gerir ok takann að vista
        chooser.setApproveButtonText("vista");
        //sýnir FilChoser
        chooser.showOpenDialog(null);
        //file það sem er skrifað inn
        File f = chooser.getSelectedFile();
        if(f!=null){
            //path verður slóðinn sem er valin
            String path = f.getPath();
            //fer í fall sem vistar mynd
            ((MyndPanel)jTeikniBlad).saveImg(path);
        }
    }
